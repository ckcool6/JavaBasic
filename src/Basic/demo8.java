package Basic;

public class demo8 {
    public static void main(String[] args) {
        int week = 5;
        switch (week){
            case 1 :
                System.out.println("星期一");
                break;
            case 2:
                System.out.println("星期二");
                break;
            case 3:
                System.out.println("星期三");
                break;
            case 4:
                System.out.println("星期四");
                break;
            case 5:
                System.out.println("星期五");
                break;
            default:
                System.out.println("数据错误");
                break;
        }
    }
}
